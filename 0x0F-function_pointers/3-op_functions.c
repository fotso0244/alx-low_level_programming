/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - soutracts 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multipliess 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
